# Examples demonstrating data types and operators

# 1. Arithmetic operations
a = 7
b = 3
print("Add:", a + b)
print("Subtract:", a - b)
print("Multiply:", a * b)
print("Division:", a / b)
print("Floor division:", a // b)
print("Modulus:", a % b)
print("Power:", a ** b)

# 2. Comparison operators
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a <= b:", a <= b)

# 3. Logical operators
x = True
y = False
print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)

# 4. Membership operators
my_list = [1, 2, 3]
print("2 in my_list:", 2 in my_list)
print("4 not in my_list:", 4 not in my_list)

# 5. Identity operators
list_a = my_list
list_b = [1, 2, 3]
print("list_a is my_list:", list_a is my_list)
print("list_a is list_b:", list_a is list_b)
print("list_a == list_b:", list_a == list_b)
